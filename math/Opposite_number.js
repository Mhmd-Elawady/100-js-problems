// ================================================
// Task:
// Given a number, return its opposite (additive inverse).
// Examples:
//   1  => -1
//   14 => -14
//   -34 => 34
// ================================================

function opposite(number) {
  return -number;
}

// ---------------------
// Test
// ---------------------
console.log(opposite(1));   // -1
console.log(opposite(14));  // -14
console.log(opposite(-34)); // 34
